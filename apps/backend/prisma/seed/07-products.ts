// Generate product data for each subcategory
function generateProductsForSubcategory(
  subcategorySlug: string,
  categoryName: string,
  subcategoryName: string
): ProductData[] {
  const products: ProductData[] = [];
  const productNameList = productNames[subcategorySlug] || [];

  for (let i = 1; i <= 10; i++) {
    const basePrice = Math.floor(Math.random() * 500) + 10; // Random price between $10-$510
    const hasSalePrice = Math.random() > 0.7; // 30% chance of having a sale price
    const salePrice = hasSalePrice ? Math.floor(basePrice * 0.8) : undefined; // 20% discount

    // Use realistic product name if available, otherwise generate one
    const productName = productNameList[i - 1] || `${subcategoryName} ${i}`;

    products.push({
      name: productName,
      description: `High-quality ${productName.toLowerCase()} in the ${categoryName.toLowerCase()} category. This product offers excellent value and performance with premium materials and craftsmanship.`,
      sku: `${subcategorySlug.toUpperCase()}-${i.toString().padStart(3, '0')}`,
      isFeatured: Math.random() > 0.8, // 20% chance of being featured
      sortOrder: i,
      subcategorySlug,
      prices: [
        {
          price: basePrice,
          salePrice,
          currency: 'USD',
        },
      ],
      stock: {
        quantity: Math.floor(Math.random() * 100) + 1, // Random stock between 1-100
        reserved: 0,
        minThreshold: 5,
        maxThreshold: 200,
      },
    });
  }

  return products;
}

export async function seedProducts(): Promise<void> {
  console.log('ðŸ“¦ Seeding products...');

  const subcategories = await getSubcategories();

  let seededCount = 0;
  let priceCount = 0;

  for (const subcategory of subcategories) {
    const products = generateProductsForSubcategory(
      subcategory.slug,
      subcategory.category.name,
      subcategory.name
    );

    for (const productData of products) {
      // Create the product
      const product = await prisma.product.upsert({
        where: { sku: productData.sku },
        update: {
          name: productData.name,
          description: productData.description,
          isFeatured: productData.isFeatured,
          sortOrder: productData.sortOrder,
          categoryId: subcategory.categoryId,
          subcategoryId: subcategory.id,
        },
        create: {
          name: productData.name,
          description: productData.description,
          sku: productData.sku,
          isFeatured: productData.isFeatured,
          sortOrder: productData.sortOrder,
          categoryId: subcategory.categoryId,
          subcategoryId: subcategory.id,
        },
      });

      // Create prices for the product
      for (const priceData of productData.prices) {
        await prisma.price.create({
          data: {
            productId: product.id,
            price: priceData.price,
            salePrice: priceData.salePrice,
            currency: priceData.currency,
          },
        });
        priceCount++;
      }

      // Create stock for the product
      await prisma.stock.create({
        data: {
          productId: product.id,
          quantity: productData.stock.quantity,
          reserved: productData.stock.reserved,
          minThreshold: productData.stock.minThreshold,
          maxThreshold: productData.stock.maxThreshold,
          isInStock: true,
        },
      });

      seededCount++;
    }
  }

  console.log(`âœ… Seeded ${seededCount} products with ${priceCount} prices`);
}

export async function getProducts() {
  return await prisma.product.findMany({
    include: {
      category: true,
      subcategory: true,
      prices: true,
    },
  });
}
